export function getFeeBreakdown(price) {
  const p = parseFloat(price)
  if (isNaN(p) || p <= 0) return { fee: 0, youGet: 0, causeAmount: 0, opsAmount: 0 }
  const fee = p * 0.04 + 0.50
  const opsAmount = fee * 0.4
  const causeAmount = fee * 0.6
  const youGet = p - fee
  return {
    fee: fee.toFixed(2),
    youGet: youGet.toFixed(2),
    causeAmount: causeAmount.toFixed(2),
    opsAmount: opsAmount.toFixed(2),
  }
}
